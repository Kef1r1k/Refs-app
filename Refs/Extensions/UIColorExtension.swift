        #colorLiteral(red: 0.01568627451, green: 0, blue: 0.7215686275, alpha: 1)
    }
    
    static var lightBlue: UIColor {
        #colorLiteral(red: 0.8470588235, green: 0.8431372549, blue: 1, alpha: 1)
